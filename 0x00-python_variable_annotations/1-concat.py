#!/usr/bin/env python3
"""
This module contains function for handling
two strings and concatinate them together.
"""


def concat(str1: str, str2: str) -> str:
    """
    Returns concatinate of two strings.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: concatinate str1 and str2.
    """
    return str1 + str2
